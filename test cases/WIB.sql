select avg(price)
from Purchases p, item i , users u
where p.user_id = u.user_id and p.item_id = i.item_id
and u.age >= 26 and u.age <= 35;
--самый прибыльный месяц не смотря на год
select EXTRACT('MONTH' FROM CAST(p.date AS DATE)) AS month, sum(price) sales
from Purchases p, item i , users u
where p.user_id = u.user_id and p.item_id = i.item_id
and age >= 35
group by month
order by sales desc
limit 1;
--самый прибыльный месяц и год
select DATE_TRUNC('MONTH', CAST(p.date AS DATE)) AS month, sum(price) sales
from Purchases p, item i , users u
where p.user_id = u.user_id and p.item_id = i.item_id
and age >= 35
group by month
order by sales desc
limit 1;
--какой товар обеспечивает дает наибольший вклад в выручку за последний год
select i.item_id,sum(price) sales
from Purchases p, item i
where p.item_id = i.item_id
and CAST(p.date AS DATE) >= CURRENT_DATE - INTERVAL '1 year'
AND CAST(p.date AS DATE) <= CURRENT_DATE
group by i.item_id
order by sales desc
limit 1;
--топ-3 товаров по выручке и их доля в общей выручке за любой год
SELECT i.item_id, SUM(price) sales, (SUM(price) / (SUM(SUM(price)) OVER ()) * 100) PERCENT
from Purchases p, item i
where p.item_id = i.item_id
group by i.item_id
order by percent desc
limit 3;

